namespace CrowdRunner
{
    // Историческая эпоха (прогрессия уровней, см. диздок п.3).
    public enum Epoch
    {
        Primitive = 0,   // Первобытность — палки, камни
        Medieval = 1,    // Средневековье — луки, мечи, катапульты
        Gunpowder = 2,   // Пороховая эпоха — мушкеты, пушки
        WorldWar = 3     // Вторая мировая — винтовки, пулемёты, танки
    }

    // Тип ворот на полосе (диздок п.2.1).
    public enum GateType
    {
        Weapon = 0,   // Левая полоса — урон/смена оружия
        Units = 1     // Правая полоса — пополнение отряда
    }

    // Как ворота меняют параметр.
    public enum GateOp
    {
        Add = 0,        // прибавить значение
        Multiply = 1,   // умножить
        Subtract = 2    // отнять (минус-ворота для риска)
    }

    // Класс/тип оружия. Меняет визуал снаряда и тип атаки (диздок п.2.3).
    public enum WeaponType
    {
        Stick = 0,      // первобытное метание
        Bow = 1,        // лук
        Musket = 2,     // мушкет / пушка
        Rifle = 3       // винтовка / пулемёт
    }

    // Тип улучшения в главном меню (диздок п.4.1).
    public enum UpgradeType
    {
        Damage = 0,        // базовый урон оружия
        StartUnits = 1,    // стартовое количество юнитов
        FireRate = 2,      // скорость стрельбы
        Volley = 3         // количество выстрелов (залповость)
    }

    // Состояние игрового потока.
    public enum GamePhase
    {
        Menu = 0,
        Running = 1,
        GameOver = 2,
        Win = 3
    }
}
